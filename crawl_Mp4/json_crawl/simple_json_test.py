def get_data():
    res = requests.get(url=url, headers=headers)
    res.raise_for_status()
    res.encoding = res.apparent_encoding
    html = res.text
    print(type(html))
    data = json.loads(html)
    movies = data['subjects']
    for movie in movies[0:20]:
        print(movie['title'], movie['rate'])


